library(igraph)

####################################################################################
### Construir grafo e Matriz de Adjacência Estocástica Transposta.
####################################################################################

g <- graph(c(1, 2, 1, 3, 2, 3, 2, 4, 3, 4, 4, 1, 4, 2, 4, 3), directed=TRUE)

M = get.adjacency(g, sparse = FALSE)
Mt = t(M / rowSums(M))
n = nrow(Mt)

####################################################################################
### Resolver o problema por iterações de cálculo matricial.
####################################################################################

v = matrix(data=rep(1/n, n), nrow=n, ncol=1)

Mt%*%v
Mt%*%(Mt%*%v)
Mt%*%(Mt%*%(Mt%*%v))
Mt%*%(Mt%*%(Mt%*%(Mt%*%v)))
Mt%*%(Mt%*%(Mt%*%(Mt%*%(Mt%*%v))))
Mt%*%(Mt%*%(Mt%*%(Mt%*%(Mt%*%(Mt%*%v)))))
Mt%*%(Mt%*%(Mt%*%(Mt%*%(Mt%*%(Mt%*%(Mt%*%v))))))

####################################################################################
### Resolver o problema por teoria de Autovalores e Autovetores.
####################################################################################

e = eigen(Mt)
r <- e$vec[,1]
r <- as.numeric(r) / sum(as.numeric(r))
